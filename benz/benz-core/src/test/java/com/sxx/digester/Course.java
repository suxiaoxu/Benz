package com.sxx.digester;

public class Course {
    private String id;
    private String name;

    public Course() {
    }

    public String getId() {
        return id;
    }

    public void setId(String newId) {
        id = newId;
    }
    public String getName() {
        return name;
    }

    public void setName(String newName) {
        name = newName;
    }
}

